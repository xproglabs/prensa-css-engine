export function generateMaxHeight(value) {
  if (!value) return ''
  return `max-height: ${value};`
}
export function generateMaxWidth(value) {
  if (!value) return ''
  return `max-width: ${value};`
}
export function generateMinHeight(value) {
  if (!value) return ''
  return `min-height: ${value};`
}
export function generateMinWidth(value) {
  if (!value) return ''
  return `min-width: ${value};`
}